export type TCategory =
  | "TAPAS_63"
  | "TAPAS_70"
  | "TUBOS"
  | "ACCESORIOS"
  | "COMBOS"
  | "CAZUELAS";
export interface Item {
  id: string;
  name: string;
  price: number;
  description: String;
  category: TCategory;
  srcs: any[];
}

const getAllItems = (): Item[] => {
  const allItems: Item[] = [];
  const PRODUCTS_IMAGES_ENTRIES = Object.entries(PRODUCTOS_IMAGES);

  PRODUCTS_IMAGES_ENTRIES.forEach(([category, item]) => {
    Object.entries(item).forEach(([name, itemInfo]) => {
      allItems.push({
        id: `${category}-${name}`,
        name: toTitleCase(name),
        price: itemInfo.price,
        description: itemInfo.description,
        category: category as TCategory,
        srcs: itemInfo.imageSrcs,
      });
    });
  });

  return allItems;
};

export const allItems = getAllItems();
